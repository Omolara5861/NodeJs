const os = require('os');

//Checking Platform
console.log(os.platform());

//Checking the CPU Architecture
console.log(os.arch());

//Checking the Core Info
console.log(os.cpus());

//Checking the Free Memory
console.log(os.freemem());

//Checking the Total Memory
console.log(os.totalmem());

//Checking the Uptime
console.log(os.uptime());

//Checking the Home Directory
console.log(os.homedir());

